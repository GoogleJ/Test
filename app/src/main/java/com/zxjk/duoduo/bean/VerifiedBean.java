package com.zxjk.duoduo.bean;

public class VerifiedBean {
    private String number;
    private String realName;
    private String type;
    private String pictureFront;
    private String pictureReverse;
    private String pictureHand;
    private String state;

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

 
    

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getRealName() {
        return realName;
    }

    public void setRealName(String realName) {
        this.realName = realName;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getPictureFront() {
        return pictureFront;
    }

    public void setPictureFront(String pictureFront) {
        this.pictureFront = pictureFront;
    }

    public String getPictureReverse() {
        return pictureReverse;
    }

    public void setPictureReverse(String pictureReverse) {
        this.pictureReverse = pictureReverse;
    }

    public String getPictureHand() {
        return pictureHand;
    }

    public void setPictureHand(String pictureHand) {
        this.pictureHand = pictureHand;
    }
}
